CREATE USER IF NOT EXISTS 'admission'@'localhost' IDENTIFIED BY 'admission123';
CREATE USER IF NOT EXISTS 'admission'@'127.0.0.1' IDENTIFIED BY 'admission123';
GRANT ALL PRIVILEGES ON admission_platform.* TO 'admission'@'localhost';
GRANT ALL PRIVILEGES ON admission_platform.* TO 'admission'@'127.0.0.1';
FLUSH PRIVILEGES;
SQL

sudo useradd --system --home /opt/gaokao --shell /usr/sbin/nologin gaokao 2>/dev/null || true
sudo mkdir -p /opt/gaokao/config /opt/gaokao/data /var/log/gaokao /var/www/gaokao/user /var/www/gaokao/admin

sudo systemctl stop gaokao-api 2>/dev/null || true
sudo cp "$RELEASE_DIR/backend/gaokao-api.jar" /opt/gaokao/app.jar
sudo cp "$RELEASE_DIR/deploy/application-prod.yml" /opt/gaokao/config/application-prod.yml
sudo rm -rf /opt/gaokao/data/organized/modeling_outputs
sudo mkdir -p /opt/gaokao/data
sudo cp -R "$RELEASE_DIR/data/." /opt/gaokao/data/
sudo rm -rf /var/www/gaokao/user /var/www/gaokao/admin
sudo mkdir -p /var/www/gaokao/user /var/www/gaokao/admin
sudo cp -R "$RELEASE_DIR/frontend/user/." /var/www/gaokao/user/
sudo cp -R "$RELEASE_DIR/frontend/admin/." /var/www/gaokao/admin/

sudo chown -R gaokao:gaokao /opt/gaokao /var/log/gaokao
sudo chown -R www-data:www-data /var/www/gaokao

sudo cp "$RELEASE_DIR/deploy/gaokao-api.service" /etc/systemd/system/gaokao-api.service
sudo cp "$RELEASE_DIR/deploy/gaokao-nginx.conf" /etc/nginx/sites-available/gaokao
sudo ln -sf /etc/nginx/sites-available/gaokao /etc/nginx/sites-enabled/gaokao
sudo rm -f /etc/nginx/sites-enabled/default

sudo systemctl daemon-reload
sudo systemctl enable --now gaokao-api
sudo nginx -t
sudo systemctl restart nginx

echo "Waiting for backend..."
for i in $(seq 1 40); do
  if curl -fsS http://127.0.0.1:8080/api/v1/recommendations/search \
    -H 'Content-Type: application/json' \
    --data-raw '{"year":2026,"educationLevel":"VOCATIONAL","score":410,"rank":430178,"subjects":["历史","生物","地理"],"pageNo":1,"pageSize":1,"recommendationCount":1}' >/dev/null; then
    break
  fi
  if [[ "$i" -eq 40 ]]; then
    sudo journalctl -u gaokao-api -n 120 --no-pager
    exit 1
  fi
  sleep 3
done

mysql -u admission -padmission123 admission_platform < "$RELEASE_DIR/sql/seed_score_rank_2026.sql"
mysql -u admission -padmission123 admission_platform < "$RELEASE_DIR/sql/seed_enrollment_plan_2026.sql"

curl -fsS http://127.0.0.1:8080/api/v1/recommendations/search \
  -H 'Content-Type: application/json' \
  --data-raw '{"year":2026,"educationLevel":"VOCATIONAL","score":410,"rank":430178,"subjects":["历史","生物","地理"],"pageNo":1,"pageSize":1,"recommendationCount":1}' >/dev/null
curl -fsS http://127.0.0.1:8080/api/v1/recommendations/search \
  -H 'Content-Type: application/json' \
  --data-raw '{"year":2026,"educationLevel":"UNDERGRADUATE","score":520,"rank":140000,"subjects":["历史","生物","地理"],"pageNo":1,"pageSize":1,"recommendationCount":1}' >/dev/null
curl -fsS http://127.0.0.1/health

echo
echo "Deployment finished."
INSTALL
chmod +x "$BUILD_DIR/deploy/install.sh"

tar -C /tmp -czf "$TARBALL" "$RELEASE_NAME"

echo "==> Uploading release to ${REMOTE}"
scp -i "$SSH_KEY" "$TARBALL" "${REMOTE}:/tmp/"

echo "==> Installing on server"
ssh -i "$SSH_KEY" "$REMOTE" "set -e; rm -rf /tmp/${RELEASE_NAME}; tar -xzf /tmp/${RELEASE_NAME}.tar.gz -C /tmp; cd /tmp/${RELEASE_NAME}; bash deploy/install.sh"

echo "==> Verifying public endpoints"
curl --noproxy '*' -fsS "http://${REMOTE_HOST}/health"
curl --noproxy '*' -fsSI "http://${REMOTE_HOST}/" >/dev/null
curl --noproxy '*' -fsSI "http://${REMOTE_HOST}/admin/" >/dev/null
curl --noproxy '*' -fsS "http://${REMOTE_HOST}/api/v1/recommendations/search" \
  -H 'Content-Type: application/json' \
  --data-raw '{"year":2026,"educationLevel":"VOCATIONAL","score":410,"rank":430178,"subjects":["历史","生物","地理"],"pageNo":1,"pageSize":1,"recommendationCount":1}' >/dev/null
curl --noproxy '*' -fsS "http://${REMOTE_HOST}/api/v1/recommendations/search" \
  -H 'Content-Type: application/json' \
  --data-raw '{"year":2026,"educationLevel":"UNDERGRADUATE","score":520,"rank":140000,"subjects":["历史","生物","地理"],"pageNo":1,"pageSize":1,"recommendationCount":1}' >/dev/null

echo
echo "Done: http://${REMOTE_HOST}/"
echo "Admin: http://${REMOTE_HOST}/admin/"
