#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT_DIR"

REMOTE_HOST="${REMOTE_HOST:-49.233.22.119}"
REMOTE_USER="${REMOTE_USER:-ubuntu}"
SSH_KEY="${SSH_KEY:-gk.pem}"
REMOTE="${REMOTE_USER}@${REMOTE_HOST}"
RUN_GIT=0
COMMIT_MESSAGE="${COMMIT_MESSAGE:-Deploy production update}"

usage() {
  cat <<'USAGE'
Usage:
  scripts/deploy_prod.sh [--push] [--message "commit message"]

Environment overrides:
  REMOTE_HOST=49.233.22.119
  REMOTE_USER=ubuntu
  SSH_KEY=gk.pem

Examples:
  scripts/deploy_prod.sh
  scripts/deploy_prod.sh --push --message "Update recommendation page"

Notes:
  - The SSH key is only used locally with ssh/scp -i; it is never packaged.
  - --push runs: git add -A, git commit, git push.
  - Without --push, the script deploys your current local code only.
USAGE
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --push)
      RUN_GIT=1
      shift
      ;;
    --message|-m)
      COMMIT_MESSAGE="${2:?Missing commit message}"
      shift 2
      ;;
    --help|-h)
      usage
      exit 0
      ;;
    *)
      echo "Unknown argument: $1" >&2
      usage
      exit 1
      ;;
  esac
done

need_cmd() {
  command -v "$1" >/dev/null 2>&1 || {
    echo "Missing command: $1" >&2
    exit 1
  }
}

need_file() {
  [[ -f "$1" ]] || {
    echo "Missing file: $1" >&2
    exit 1
  }
}

need_cmd git
need_cmd ssh
need_cmd scp
need_cmd tar
need_cmd mvn
need_cmd pnpm
need_cmd python3

need_file "$SSH_KEY"
need_file "data/organized/modeling_outputs/specialized_rank_model_2026.csv"
need_file "data/organized/modeling_inputs/extracted_csv/shandong_score_rank_2026.csv"
need_file "data/organized/modeling_inputs/extracted_csv/shandong_2026_bachelor_3in1.csv"

chmod 600 "$SSH_KEY" >/dev/null 2>&1 || true

if [[ "$RUN_GIT" -eq 1 ]]; then
  echo "==> Committing and pushing current changes"
  git add -A
  if git diff --cached --quiet; then
    echo "No staged changes; skip commit."
  else
    git commit -m "$COMMIT_MESSAGE"
  fi
  git push
fi

GIT_SHA="$(git rev-parse --short HEAD 2>/dev/null || date +%Y%m%d%H%M%S)"
RELEASE_NAME="gaokao-release-${GIT_SHA}-$(date +%Y%m%d%H%M%S)"
BUILD_DIR="/tmp/${RELEASE_NAME}"
TARBALL="/tmp/${RELEASE_NAME}.tar.gz"

echo "==> Building backend"
mvn -f backend/pom.xml -pl admission-boot -am clean install -DskipTests
mvn -f backend/pom.xml -pl admission-boot package org.springframework.boot:spring-boot-maven-plugin:3.4.4:repackage -DskipTests

echo "==> Building frontend"
(
  cd frontend
  pnpm install --frozen-lockfile
  pnpm build:user
  pnpm --filter @gaokao/admin-web exec vite build --base=/admin/
)

echo "==> Preparing release package: ${TARBALL}"
rm -rf "$BUILD_DIR" "$TARBALL"
mkdir -p \
  "$BUILD_DIR/backend" \
  "$BUILD_DIR/frontend/user" \
  "$BUILD_DIR/frontend/admin" \
  "$BUILD_DIR/data/organized/modeling_outputs" \
  "$BUILD_DIR/deploy" \
  "$BUILD_DIR/sql"

cp backend/admission-boot/target/admission-boot-1.0.0-SNAPSHOT.jar "$BUILD_DIR/backend/gaokao-api.jar"
cp -R frontend/apps/user-web/dist/. "$BUILD_DIR/frontend/user/"
cp -R frontend/apps/admin-web/dist/. "$BUILD_DIR/frontend/admin/"
cp data/organized/modeling_outputs/specialized_rank_model_2026.csv "$BUILD_DIR/data/organized/modeling_outputs/"
if [[ -f data/organized/modeling_outputs/specialized_rank_model_2026.meta.json ]]; then
  cp data/organized/modeling_outputs/specialized_rank_model_2026.meta.json "$BUILD_DIR/data/organized/modeling_outputs/"
fi

python3 - <<'PY' > "$BUILD_DIR/sql/seed_score_rank_2026.sql"
import csv
from pathlib import Path

src = Path("data/organized/modeling_inputs/extracted_csv/shandong_score_rank_2026.csv")
rows = []
with src.open("r", encoding="utf-8-sig", newline="") as f:
    reader = csv.reader(f)
    for row in reader:
        if len(row) < 3:
            continue
        try:
            score = int(str(row[0]).strip())
            segment = int(str(row[1]).strip())
            cumulative = int(str(row[2]).strip())
        except ValueError:
            continue
        rows.append((score, segment, cumulative))

print("-- 2026 score-rank seed generated by scripts/deploy_prod.sh")
print("START TRANSACTION;")
print("SET @old_score_rank_version_id = (SELECT data_version_id FROM active_data_version WHERE data_type = 'SCORE_RANK' AND year = 2026 LIMIT 1);")
print("DELETE FROM active_data_version WHERE data_type = 'SCORE_RANK' AND year = 2026;")
print("DELETE FROM score_rank_segment WHERE data_version_id IN (SELECT id FROM data_version WHERE data_type = 'SCORE_RANK' AND year = 2026);")
print("DELETE FROM data_version WHERE data_type = 'SCORE_RANK' AND year = 2026;")
print(f"INSERT INTO data_version (data_type, year, version_no, status, row_count, published_by, published_at) VALUES ('SCORE_RANK', 2026, 1, 'PUBLISHED', {len(rows)}, 0, NOW());")
print("SET @score_rank_version_id = LAST_INSERT_ID();")
print("INSERT INTO active_data_version (data_type, year, data_version_id) VALUES ('SCORE_RANK', 2026, @score_rank_version_id);")
print("INSERT INTO score_rank_segment (data_version_id, year, score, cumulative_count, segment_count) VALUES")
values = [
    f"(@score_rank_version_id, 2026, {score}, {cumulative}, {segment})"
    for score, segment, cumulative in rows
]
print(",\n".join(values) + ";")
print("COMMIT;")
PY

python3 scripts/generate_undergraduate_seed_sql.py \
  --input data/organized/modeling_inputs/extracted_csv/shandong_2026_bachelor_3in1.csv \
  > "$BUILD_DIR/sql/seed_enrollment_plan_2026.sql"

cat > "$BUILD_DIR/deploy/application-prod.yml" <<'YAML'
